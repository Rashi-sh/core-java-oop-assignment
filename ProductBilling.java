class ProductBilling {

    // 1 product bill
    double calculateBill(double price1) {

        return price1;
    }

    // 2 products bill
    double calculateBill(double price1, double price2) {

        return price1 + price2;
    }

    // 3 products bill
    double calculateBill(double price1,
            double price2,
            double price3) {

        return price1 + price2 + price3;
    }
}